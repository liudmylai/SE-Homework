// Display the average of five numbers: Create five separate variables holding different numbers and find the average
public class AverageOfFive {
    public static void main (String[] args) {
        int num1 = 5, num2 = 3, num3 = 10, num4 = 7, num5 = 15;
        int averageNum = (num1 + num2 + num3 + num4 + num5) / 5;
        System.out.println("Average = " + averageNum);
    }
}